const DICTS: Record<Lang, Dict> = { fr, en };

interface I18nContextType {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
}

const I18nContext = createContext<I18nContextType | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("fr");

  useEffect(() => {
    try {
      const stored = localStorage.getItem(STORAGE_KEY) as Lang | null;
      if (stored && (stored === "fr" || stored === "en")) {
        setLangState(stored);
        return;
      }
      // Détection navigateur
      const nav =
        typeof navigator !== "undefined"
          ? navigator.language.toLowerCase().slice(0, 2)
          : "fr";
      setLangState(nav === "fr" ? "fr" : "en");
    } catch {
      /* ignore */
    }
  }, []);

  function setLang(l: Lang) {
    setLangState(l);
    try {
      localStorage.setItem(STORAGE_KEY, l);
    } catch {
      /* ignore */
    }
  }

  function t(key: string, vars?: Record<string, string | number>) {
    const dict = DICTS[lang];
    let s = dict[key] ?? key;
    if (vars) {
      for (const [k, v] of Object.entries(vars)) {
        s = s.replace(`{${k}}`, String(v));
      }
    }
    return s;
  }

  return <I18nContext.Provider value={{ lang, setLang, t }}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be inside I18nProvider");
  return ctx;
}

// =====================================================================
// Helpers — locale formatting + calendar labels
// =====================================================================

/** Returns BCP-47 locale tag aligned with the current i18n language. */
export function localeForLang(lang: Lang): string {
  return lang === "fr" ? "fr-FR" : "en-US";
}

/** Hook returning month/day labels in the current language. */
export function useDateLabels(): { months: string[]; days: string[]; locale: string } {
  const { t, lang } = useI18n();
  const months = Array.from({ length: 12 }, (_, i) => t(`calendar.month.${i}`));
  const days = Array.from({ length: 7 }, (_, i) => t(`calendar.day.${i}`));
  return { months, days, locale: localeForLang(lang) };
}
